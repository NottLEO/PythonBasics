Zahl1 = input ("Geben Sie eine Zahl ein: ")
Zahl2 = input ("Geben Sie eine weitere Zahl ein: ")

if Zahl1 > Zahl2:
    print ("Zahl1 ist grösser als Zahl 2")
elif Zahl1 < Zahl2:
    print ("Zahl 2 ist grösser als Zahl 1")
elif Zahl1 == Zahl2:
    print ("Zahl 1 ist gleich Zahl 2")
else:
    print ("Das ist keine Zahl")